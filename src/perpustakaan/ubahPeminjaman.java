    private void tampil_buku(){
        Object []baris = {"Kode Buku", "Judul Buku", "Kategori"};
        tbl1 = new DefaultTableModel(null, baris) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTableBuku.setModel(tbl1);
        
        try{
            Connection con = new perpustakaan.koneksi().GetConnection();
            String sql = "SELECT * FROM buku ORDER BY kode_buku ASC";
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {
                String kode = rs.getString("kode_buku");
                String judul = rs.getString("judul_buku");
                String kategori = rs.getString("kategori");
                
                String[] data = {kode, judul, kategori};
                tbl1.addRow(data);
            }
            
        }catch(Exception e){
            System.err.println("Terjadi kesalahan:"+e);
        }
    }
    
