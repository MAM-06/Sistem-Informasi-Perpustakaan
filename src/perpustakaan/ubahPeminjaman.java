    public void tampilDataPeminjaman(String Id){
        try {
            Connection con = new perpustakaan.koneksi().GetConnection();
            String sql = "select*from peminjaman where id like '%"+Id+"%'";
            java.sql.Statement st = con.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                jTidAnggota.setText(rs.getString("id_anggota"));
                jTnama.setText(rs.getString("nama"));
                jTemail.setText(rs.getString("email"));
                jTkodeBuku.setText(rs.getString("kode_buku"));
                jTjudulBuku.setText(rs.getString("judul_buku"));
                jTtanggalPinjam.setText(rs.getString("tanggal_pinjam"));
            }
            
        } catch (Exception e) {
            System.err.println("Terjadi kesalahan:"+e);
        }
    } 

